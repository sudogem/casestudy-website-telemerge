  <h1 class="cap2 capline" >What is medical transcription ?</h1>
  	<p class="text" >Medical transcription, also known as MT, is an allied health profession which encompasses the process of transcription or converting voice-recorded reports as dictated by physicians and/or other healthcare professionals into text format.</p>
<br />
 <p class="text">Medical transcription encompases the MT, performing document typing and formatting functions according to an established criteria or format, transcribing the spoken word of the patient's care information into a written, easily readable form. MT requires correct spelling of all terms and words, (occasionally) correcting medical terminology or dictation errors. MTs also edit the transcribed documents, print or return the completed documents in a timely fashion. All transcription reports must comply with medico-legal concerns, policies and procedures, and laws under patient confidentiality.</p>
<br />
<p class="text" >
In transcribing directly for a doctor or a group of physicians, there are specific formats and report types used, dependent on that doctor's speciality of practice, although history and physical exams or consults are mainly utilized. In most of the off-hospital sites, independent medical practices perform consultations as a second opinion, pre-surgical exams, and as IMEs (Independent Medical Examinations) for liability insurance or disability claims. Private practice family doctors rarely utilize a medical transcriptionist, preferring to keep their patient's records in a handwritten format.
</p>

<p class="text" >
Currently, a growing number of medical providers send their dictation by digital voice files; some are utilizing a slowly developing, somewhat unreliable method of transcription called speech or voice recognition. Speech recognition is still a nascent technology that loses much in translation. For dictators to utilize the software, they must first train the program to recognize their spoken words. Dictation is read into the database and the program continuously "learns" the spoken words and phrases.
</p>
