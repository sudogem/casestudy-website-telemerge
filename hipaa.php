	<h1 class="cap2 capline" >What is HIPAA?</h1>

	<p class="text">

	Health Insurance Portability and Accountability Act ( HIPPA ) is Federal regulations establishing

	national standards for health care information. It is intended to



	reduce the costs and administrative burdens of health care provider by making possible standardized, electronic transmission of many administrative and financial transactions that are currently carried out manually on paper to improve the efficiency and effectiveness of the health care system. It also gives patients greater access to their own medical records and more control over how their personally identifiable health information is used and addresses the obligations of healthcare providers and health plans to protect health information. </p>

	<p class="text">The Health Insurance Portability & Accountability Act of 1996 (HIPAA), Public Law was passed by Congress:</p>

 	<div class="commonlist-items" >

	<ul >

		<li>To improve portability and continuity of health insurance coverage in the group and individual markets</li>

		<li>To combat waste, fraud, and abuse in health insurance and health care delivery</li>

		<li>To reduce costs and the administrative burdens of health care by improving efficiency and effectiveness of the health care system by standardizing the interchange of electronic data for specified administrative and financial transactions</li>

		<li>To ensure protecting the privacy of Americans' personal health records by protecting the security and confidentiality of health care information</li>

	</ul>

	</div>

